print("\nExercício 3 - Nota\n")

i = 0
while i < 10:
    nt1 = float(input("Insira a 1° nota: "))
    print("")
    nt2 = float(input("Insira a 2° nota: "))
    print("")
    
    media = (nt1 + nt2) / 2
    i = i + 1
    
    print("A média do aluno é:",media)
    print("")
    print("------------------------\n")
